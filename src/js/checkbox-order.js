function myFunction() {
  // Get the checkbox
  var checkBox = document.getElementById("myCheck");
  // Get the output flavors
  var flavors = document.getElementById("flavors");

  // If the checkbox is checked, display the output flavors
  if (checkBox.checked == true){
    flavors.style.display = "flex";
  } else {
    flavors.style.display = "none";
  }
}

